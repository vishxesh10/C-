#include <iostream>
using namespace std;

int main() {
    int integer = 10;
    float floatingPoint = 5.5;
    char character = 'A';
    bool boolean = true;
    
    cout << "Integer: " << integer << endl;
    cout << "Float: " << floatingPoint << endl;
    cout << "Character: " << character << endl;
    cout << "Boolean: " << boolean << endl;
    
    return 0;
}